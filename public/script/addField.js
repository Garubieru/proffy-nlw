let scheduleItems = document.querySelector('#schedule-items')
let item = document.querySelector('.schedule-item');
let button = document.querySelector('#add-time');

button.addEventListener('click', addTime)

function addTime() {
    // Duplicate the container
    const newItemContainer = item.cloneNode(true);

    // Catching the newItemContainer(duplicated item) all inputs tag.
    const newItemInputs = newItemContainer.querySelectorAll('input')
    // querySelectorAll returns a list contaning all the inputs tags.

    /*for (let i in newItemInputs) {
        newItemInputs[i].value = '' Clean each newItemInputs values on the list generated by querySelectorAll
    }*/

    // For each(Para cada) input in newItemInputs, clean the value of the inputs
    newItemInputs.forEach(function(input) {
        // <input type="time" name="time_from[]" required>
        // <input type="time" name="time_to[]" required>
        input.value = ''
    })

    // Append the newItemContainer in schedule itens class on html.
    scheduleItems.appendChild(newItemContainer)

}